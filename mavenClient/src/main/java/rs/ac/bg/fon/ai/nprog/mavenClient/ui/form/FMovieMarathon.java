    private void fillTable() {
        ShowtimeTableModel showtimeTableModel = new ShowtimeTableModel();
        jTblShowtimesMarathon.setModel(showtimeTableModel);
    }

    private boolean checkMovies(List<Showtime> showtimes, Showtime sh) {
        for (Showtime s : showtimes) {
            if (s == sh) {
                return false;
            }
            if (s.getMovie().equals(sh.getMovie())) {
                return false;
            }
        }
        return true;
    }

    public List<Showtime> getAddedShowtimes() {
        return addedShowtimes;
    }

    public void addShowtimeToMarathon(Showtime s) {
        addedShowtimes.add(s);
        ShowtimeTableModel stm = (ShowtimeTableModel) jTblShowtimesMarathon.getModel();
        stm.addShowtime(s);
    }

