    private void jBtnSaveMovieMarathonActionPerformed(java.awt.event.ActionEvent evt) {                                                      
        ShowtimeTableModel showtimeTableModel = (ShowtimeTableModel) jTblShowtimesMarathon.getModel();
        List<Showtime> showtimes = showtimeTableModel.getAllShowtimes();
        if (jTxtMarathonName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Unesite naziv filmskog maratona.");
            return;
        }
        if (showtimes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate dodati projekcije za filmski maraton.");
            return;
        }
        String name = jTxtMarathonName.getText();
        MovieMarathon movieMarathon = new MovieMarathon(name, controller.Controller.getInstance().getCurrentUser());
        try {
            Controller.getInstance().saveMovieMarathon(movieMarathon, showtimes,this);

        } catch (Exception ex) {
            Logger.getLogger(FMovieMarathon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                                     

    private void jBtnRemoveShowtimeActionPerformed(java.awt.event.ActionEvent evt) {                                                   
        int row = jTblShowtimesMarathon.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Morate odabrati projekciju.");
            return;
        } else {
            ShowtimeTableModel showtimeTableModel = (ShowtimeTableModel) jTblShowtimesMarathon.getModel();
            showtimeTableModel.removeShowtime(row);
        }
    }                                                  

