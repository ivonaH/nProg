    private void jBtnSaveReservationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSaveReservationActionPerformed
        if(showtime==null){
            JOptionPane.showMessageDialog(this, "Morate odabrati projekciju!");
            return;
        }
        Reservation reservation = new Reservation(jTxtNameLastname.getText(), jTxtEmail.getText(), Controller.getInstance().getCurrentUser(), showtime);
        try {
            Controller.getInstance().saveReservation(reservation, this);
        } catch (Exception ex) {
            Logger.getLogger(FReservation.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jBtnSaveReservationActionPerformed

