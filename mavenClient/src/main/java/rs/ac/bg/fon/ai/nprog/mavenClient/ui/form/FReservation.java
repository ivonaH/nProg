    private void prepareShowtime() {
        if (showtime != null) {
            jTxtMovie.setText(showtime.getMovie().toString());
            jTxtShowtime.setText(showtime.toString());
            jtxtDate.setText(showtime.getDate() + "");

        }
    }

    public void reservationIsNotSaved() {
        JOptionPane.showMessageDialog(this, "Sistem nije zapamtio rezervaciju.");
    }

    public void reservationIsSaved() {
        JOptionPane.showMessageDialog(this, "Sistem je zapamtio rezervaciju.");

        dispose();
    }

