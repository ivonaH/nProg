    private void jBtnSaveReservationActionPerformed(java.awt.event.ActionEvent evt) {                                                    
        if (showtime == null) {
            JOptionPane.showMessageDialog(this, "Morate odabrati projekciju!");
            return;
        }
        Reservation reservation = new Reservation(jTxtNameLastname.getText(), jTxtEmail.getText(), controller.Controller.getInstance().getCurrentUser(), showtime);
        try {
            Controller.getInstance().saveReservation(reservation, this);
        } catch (Exception ex) {
            Logger.getLogger(FReservation.class.getName()).log(Level.SEVERE, null, ex);
        }

    }                                                   

    private void jBttnSelectShowtimeActionPerformed(java.awt.event.ActionEvent evt) {                                                    
        FShowtimeSearch fShowtimeSearch = new FShowtimeSearch(null, true, FormMode.FORM_SELECT);
        fShowtimeSearch.setVisible(true);
        try {
            showtime = fShowtimeSearch.getShowtime();
        } catch (Exception ex) {
        }

        prepareShowtime();
    }                                                   

