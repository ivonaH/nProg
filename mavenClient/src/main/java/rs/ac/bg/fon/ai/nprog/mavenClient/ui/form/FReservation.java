    private void prepareShowtime() {
        if (showtime != null) {
            jTxtMovie.setText(showtime.getMovie().toString());
            jTxtShowtime.setText(showtime.toString());
            jtxtDate.setText(showtime.getDate() + "");

        }
    }

    public void reservationIsNotSaved() {
        JOptionPane.showMessageDialog(this, "Sistem nije zapamtio rezervaciju.");
    }

    public void reservationIsSaved() {
        JOptionPane.showMessageDialog(this, "Sistem je zapamtio rezervaciju.");

        dispose();
    }

    public void invalidEmail() {
        JOptionPane.showMessageDialog(this, "Unesite ispravan email.");
    }

    private void prepareForm(int id) {
        if (formMode == FormMode.FORM_VIEW) {
            jBttnSelectShowtime.setVisible(false);
            jBtnSaveReservation.setVisible(false);
            Controller.getInstance().getReservationWithId(id, this);
        }else if(formMode==FormMode.FORM_ADD){
            jLblId.setVisible(false);
            jTxtId.setVisible(false);
        }

    }

    public void reservationLoaded(Reservation reservation) {
        jTxtMovie.setText(reservation.getShowtime().getMovie().toString());
        jTxtEmail.setText(reservation.getEmail());
        jTxtNameLastname.setText(reservation.getNameLastname());
        jTxtShowtime.setText(reservation.getShowtime().toString());
        jtxtDate.setText(reservation.getShowtime().getDate().toString());
        jTxtId.setText(reservation.getReservationId()+"");
        
        jTxtId.setEditable(false);
        jTxtMovie.setEditable(false);
        jTxtEmail.setEditable(false);
        jTxtNameLastname.setEditable(false);
        jTxtShowtime.setEditable(false);
        jtxtDate.setEditable(false);
    }

    public void reservationNotLoaded() {
        JOptionPane.showMessageDialog(this, "Rezervacija nije ucitana.");
    }

