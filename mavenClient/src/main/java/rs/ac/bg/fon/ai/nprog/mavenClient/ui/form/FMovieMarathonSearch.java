    private void fillForm() throws Exception {
        Controller.getInstance().getAllMovieMarathons(this);

    }

    public void fillTable(List<MovieMarathon> movieMarathons) {
        if (movieMarathons.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe filmski maraton po zadatoj vrednosti.");
            return;
        }
        JOptionPane.showMessageDialog(this, "Pretraga je završena.");
        MovieMarathonTableModel mmtm = new MovieMarathonTableModel(movieMarathons);
        jTblMovieMarathons.setModel(mmtm);
        jTblMovieMarathons.getColumnModel().getColumn(0).setPreferredWidth(5);

    }

    public void marathonLoadingFailed() {
        JOptionPane.showMessageDialog(this, "Ucitavanje filmskih maratona nije uspelo.");
    }

    public void setShowtimes(List<Showtime> showtimes) {
        try {
            JOptionPane.showMessageDialog(this, "Odabrani maraton je prikazan.");
            for (int i = 0; i < showtimes.size(); i++) {
                if (i == 0) {
                    jLblMarathonName.setText(jLblMarathonName.getText() + "\n Datum: " + showtimes.get(i).getDate() + "\n");
                }

                jTxtAreaDetails.setText(jTxtAreaDetails.getText() + "Vreme: "+showtimes.get(i).getTime() + "\n Film: " + showtimes.get(i).getMovie().getName() + "\n Sala: "+showtimes.get(i).getHall().getName()+"\n\n");
            }
        } catch (Exception ex) {
            Logger.getLogger(FMovieMarathonSearch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void showtimesLoadingFailed() {
        JOptionPane.showMessageDialog(this, "Ucitavanje projekcija nije uspelo.");
    }

