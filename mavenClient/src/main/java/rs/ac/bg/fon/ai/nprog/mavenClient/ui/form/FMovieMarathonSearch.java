    private void jBtnDetailsActionPerformed(java.awt.event.ActionEvent evt) {                                            
        jLblMarathonName.setText("");
        jTxtAreaDetails.setText("");
        int row = jTblMovieMarathons.getSelectedRow();
        if (row != -1) {
            MovieMarathonTableModel mmtm = (MovieMarathonTableModel) jTblMovieMarathons.getModel();
            MovieMarathon mm = mmtm.getSelectedMarathon(row);
            ArrayList<String> columns = new ArrayList<>();
            ArrayList<String> values = new ArrayList<>();
            columns.add("MovieMarathonId");
            values.add(mm.getMarathonId() + "");
            jLblMarathonName.setText(mm.getName());
            try {
                Controller.getInstance().getShowtimesWithCriteria(columns, values, this);
            } catch (Exception ex) {
                Logger.getLogger(FMovieMarathonSearch.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Izaberite filmski maraton.");
        }

    }                                           

    private void jBtnSearchActionPerformed(java.awt.event.ActionEvent evt) {                                           
        if (jTxtMarathonName.equals("")) {
            return;
        }
        ArrayList<String> columns = new ArrayList<>();
        ArrayList<String> values = new ArrayList<>();
        values.add(jTxtMarathonName.getText());
        columns.add("name");
        List<MovieMarathon> marathons;
        try {
            Controller.getInstance().getMovieMarathonWithCriteria(columns, values, this);

        } catch (Exception ex) {
            Logger.getLogger(FMovieMarathonSearch.class.getName()).log(Level.SEVERE, null, ex);
        }


    }                                          

